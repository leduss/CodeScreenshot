export const getLanguageIcon = (language: string): React.ReactNode => {
  const lang = language.toLowerCase();

  // Check for exact match first
  if (languageIcons[lang]) {
    return languageIcons[lang];
  }

  // Check for partial matches
  if (
    lang === 'tsx' ||
    (lang.includes('typescript') && lang.includes('react'))
  ) {
    return languageIcons.tsx;
  }
  if (lang.includes('typescript') || lang.includes('ts')) {
    return languageIcons.typescript;
  }
  if (lang.includes('javascript') && lang.includes('react')) {
    return languageIcons.javascriptreact;
  }
  if (lang.includes('javascript') || lang.includes('jsx')) {
    return languageIcons.javascript;
  }
  if (lang.includes('python') || lang.includes('py')) {
    return languageIcons.python;
  }
  if (lang.includes('rust')) {
    return languageIcons.rust;
  }
  if (lang.includes('go') || lang.includes('golang')) {
    return languageIcons.go;
  }
  if (lang.includes('java')) {
    return languageIcons.java;
  }
  if (lang.includes('c++') || lang.includes('cpp')) {
    return languageIcons['c++'];
  }
  if (lang.includes('c ') || lang === 'c') {
    return languageIcons.c;
  }
  if (lang.includes('ruby') || lang.includes('rb')) {
    return languageIcons.ruby;
  }
  if (lang.includes('php')) {
    return languageIcons.php;
  }
  if (lang.includes('swift')) {
    return languageIcons.swift;
  }
  if (lang.includes('kotlin') || lang.includes('kt')) {
    return languageIcons.kotlin;
  }
  if (lang.includes('html')) {
    return languageIcons.html;
  }
  if (lang.includes('css')) {
    return languageIcons.css;
  }

  return languageIcons.default;
};

export const getLanguageName = (language: string): string => {
  const langMap: Record<string, string> = {
    typescript: 'TS',
    typescriptreact: 'TSX',
    javascript: 'JS',
    javascriptreact: 'JSX',
    python: 'PY',
    rust: 'RS',
    go: 'GO',
    java: 'JAVA',
    c: 'C',
    cpp: 'C++',
    'c++': 'C++',
    ruby: 'RB',
    php: 'PHP',
    swift: 'SW',
    kotlin: 'KT',
    html: 'HTML',
    css: 'CSS',
  };

  const lang = language.toLowerCase();

  // Check partial matches
  if (lang.includes('typescript') || lang.includes('ts')) {
    if (lang.includes('react') || lang.includes('tsx')) return 'TSX';
    return 'TS';
  }
  if (lang.includes('javascript') || lang.includes('js')) {
    if (lang.includes('react') || lang.includes('jsx')) return 'JSX';
    return 'JS';
  }
  if (lang.includes('python') || lang.includes('py')) return 'PY';
  if (lang.includes('rust')) return 'RS';
  if (lang.includes('go') || lang.includes('golang')) return 'GO';
  if (lang.includes('java')) return 'JAVA';
  if (lang.includes('c++') || lang.includes('cpp')) return 'C++';
  if (lang.includes('c ') || lang === 'c') return 'C';
  if (lang.includes('ruby') || lang.includes('rb')) return 'RB';
  if (lang.includes('php')) return 'PHP';
  if (lang.includes('swift')) return 'SW';
  if (lang.includes('kotlin') || lang.includes('kt')) return 'KT';
  if (lang.includes('html')) return 'HTML';
  if (lang.includes('css')) return 'CSS';

  return langMap[lang] || language.toUpperCase().slice(0, 4);
};

export const getLanguageExtension = (language: string): string => {
  const lang = language.toLowerCase();

  if (
    lang === 'tsx' ||
    (lang.includes('typescript') &&
      (lang.includes('react') || lang.includes('tsx')))
  )
    return '.tsx';
  if (lang.includes('typescript')) return '.ts';
  if (
    lang.includes('javascript') &&
    (lang.includes('react') || lang.includes('jsx') || lang === 'jsx')
  )
    return '.jsx';
  if (lang.includes('javascript')) return '.js';
  if (lang.includes('python') || lang.includes('py')) return '.py';
  if (lang.includes('rust')) return '.rs';
  if (lang.includes('go') || lang.includes('golang')) return '.go';
  if (lang.includes('java')) return '.java';
  if (lang.includes('c++') || lang.includes('cpp')) return '.cpp';
  if (lang.includes('c ') || lang === 'c') return '.c';
  if (lang.includes('ruby') || lang.includes('rb')) return '.rb';
  if (lang.includes('php')) return '.php';
  if (lang.includes('swift')) return '.swift';
  if (lang.includes('kotlin') || lang.includes('kt')) return '.kt';
  if (lang.includes('html')) return '.html';
  if (lang.includes('css')) return '.css';
  if (lang.includes('sass') || lang.includes('scss')) return '.scss';
  if (lang.includes('json')) return '.json';
  if (lang.includes('markdown') || lang.includes('md')) return '.md';
  if (lang.includes('sql')) return '.sql';
  if (lang.includes('shell') || lang.includes('bash')) return '.sh';
  if (lang.includes('yaml') || lang.includes('yml')) return '.yml';

  return '.' + lang;
};
